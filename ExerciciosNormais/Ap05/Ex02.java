package Ap05;

public class Ex02 {
    public static void main(String[] args) {
        for (int i = 100; i != 0; i -= 1 ){
            System.out.println(i);
        }
        System.out.printf("\n");
        int x = 100;
        while (x != 0){
            System.out.println(x);
            x -= 1;
        }
        System.out.printf("\n");
        int a = 100;
        do {
            System.out.println(a);
            a--;
        }
        while (a != 0);
        }
    
}
