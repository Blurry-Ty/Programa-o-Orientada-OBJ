package Ap05;

public class Ex01 {
    public static void main(String[] args) {
        for (int i = 0; i < 101; i += 1 ){
            System.out.println(i);
        }
        System.out.printf("\n");
        int x = 0;
        while (x != 100){
            System.out.println(x);
            x += 1;
        }
        System.out.printf("\n");
        int a = 0;
        do {
            System.out.println(a);
            a++;
        }
        while (a != 101);
        }
}
